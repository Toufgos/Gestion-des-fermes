package si.smart.ferme.models;

public class OccupationForm {
	
	 private int ordrevar;
	 private long famille;
	 private long sousfamille;
	 private long variete;
	 private String mode;
	 private double x;
	 private double y;
	 private int arbrenbr;
	 private int anneeplantation;
	 private String submit;
	 private long id_parce;
	 
	public int getOrdrevar() {
		return ordrevar;
	}
	public void setOrdrevar(int ordrevar) {
		this.ordrevar = ordrevar;
	}
	public long getFamille() {
		return famille;
	}
	public void setFamille(long famille) {
		this.famille = famille;
	}
	public long getSousfamille() {
		return sousfamille;
	}
	public void setSousfamille(long sousfamille) {
		this.sousfamille = sousfamille;
	}
	public long getVariete() {
		return variete;
	}
	public void setVariete(long variete) {
		this.variete = variete;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public int getArbrenbr() {
		return arbrenbr;
	}
	public void setArbrenbr(int arbrenbr) {
		this.arbrenbr = arbrenbr;
	}
	public int getAnneeplantation() {
		return anneeplantation;
	}
	public void setAnneeplantation(int anneeplantation) {
		this.anneeplantation = anneeplantation;
	}
	public String getSubmit() {
		return submit;
	}
	public void setSubmit(String submit) {
		this.submit = submit;
	}
	public long getId_parce() {
		return id_parce;
	}
	public void setId_parce(long id_parce) {
		this.id_parce = id_parce;
	}
	 
	 
}
